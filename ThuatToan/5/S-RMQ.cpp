#include<bits/stdc++.h>
#define ll long long
using namespace std;
ll n, tmp, m, ans = 0;
vector<ll> arr;
vector<vector<ll>> a;
int Max = 1e6 + 2;
int main() {
	ll ok = 0;
	ll x, y;
	ios_base::sync_with_stdio(0);
	cin.tie(0); cout.tie(0);
	cin >> n;
	a.resize(Max);
	for (int i = 0; i < n; i++){
		cin >> tmp;
		arr.push_back(tmp);
		a[arr[i]].push_back(i);
	}
	cin >> m;
	for (int i = 0; i < m; i++){
		ok = 0;
		cin >> x >> y;
		ll k = min(arr[x], arr[y]);
		for (int j = 1; j <= k; j++){
			for (auto z : a[j]) {
				if (z > y) break;
				if (x <= z && z <= y) {
					ans += j;
					ok = 1;
					break;
				}
			}
			if (ok) break;
		}
	}
	cout << ans;
	return 0;
}